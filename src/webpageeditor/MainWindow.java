    private void peopleSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_peopleSortActionPerformed
        if(peopleSort.getSelectedItem().toString() == "Faculty Members")
            filltables.completeTable(peopleTable,listOfColumns,parser.parser(listOfColumns, "facultyMembers", "people"));
        else if (peopleSort.getSelectedItem().toString() == "Current Members")
            filltables.completeTable(peopleTable,listOfColumns,parser.parser(listOfColumns, "currentMembers", "people"));
        else if (peopleSort.getSelectedItem().toString() == "Visiting Professors")
            filltables.completeTable(peopleTable,listOfColumns,parser.parser(listOfColumns, "visiting_profs", "people"));
        else if (peopleSort.getSelectedItem().toString() == "Visiting Students")
            filltables.completeTable(peopleTable,listOfColumns,parser.parser(listOfColumns, "visiting_studs", "people"));
        else if (peopleSort.getSelectedItem().toString() == "NSL Alumni")
            filltables.completeTable(peopleTable,listOfColumns,parser.parser(listOfColumns, "alums", "people"));
        
        clearButton();
    }//GEN-LAST:event_peopleSortActionPerformed

