    private void imagePreview(String path, JLabel previewLabel)
    {
        //fixing windows path issue with JAVA path
        if(!(path.contains("http") || path.contains("https") || path.contains("ww2") || path.contains("www")))
                path = "file:///"+path.replace("\\", "/");
        
        previewLabel.setIcon(null);
        previewLabel.revalidate();
        if(path.equals("") || path == null)
        {
            previewLabel.setText("Invalild image location/URL");
            return;
        }
        
        
        try
        {
            BufferedImage image = ImageIO.read(new URL(path));
            ImageIcon icon = new ImageIcon(image.getScaledInstance(previewLabel.getWidth(), previewLabel.getHeight(), Image.SCALE_DEFAULT));
            previewLabel.setIcon(icon);
            
        }catch(Exception x)
        {
            previewLabel.setText("Invalild image location/URL");
        }
       
    }
    
    
    private String createNewsLinksJSON(JTable table,String[] headers)
    {
        if(table.getRowCount() == 0)
            return null;
            
        DefaultTableModel mymodel = new DefaultTableModel();
        mymodel = (DefaultTableModel)table.getModel();
        String firstPart ="[";
        System.out.println("RowCount--->"+table.getRowCount());
        for(int outter=0;outter<table.getRowCount();outter++)
        {
            firstPart = firstPart +"{";
            for(int inner=0;inner<headers.length;inner++)
                firstPart = firstPart +"\""+headers[inner]+"\":"+"\""+fileops.specialCharacterHandler((String)mymodel.getValueAt(outter, inner))+"\",";
            
            firstPart = fileops.removeLastChar(firstPart);
            firstPart=firstPart+"},";
        }
        
        firstPart = fileops.removeLastChar(firstPart);
        firstPart = firstPart + "]";
        
        return firstPart;
    }
    
    
    

